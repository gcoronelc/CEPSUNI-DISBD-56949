
CREATE TABLE dbo.PRODUCTO
( 
	IDPRODUCTO           integer  NOT NULL ,
	NOMBRE               varchar(150)  NOT NULL ,
	PRECIO               numeric(12,2)  NOT NULL ,
	STOCK                integer  NOT NULL ,
	CONSTRAINT XPKPRODUCTO PRIMARY KEY (IDPRODUCTO ASC)
)
go



CREATE TABLE dbo.CLIENTE
( 
	IDCLIENTE            integer  NOT NULL ,
	NOMBRE               varchar(100)  NOT NULL ,
	DIRECCION            varchar(100)  NOT NULL ,
	CORREO               varchar(100)  NOT NULL ,
	TELEFONO             varchar(30)  NULL ,
	CONSTRAINT XPKCLIENTE PRIMARY KEY (IDCLIENTE ASC)
)
go



CREATE TABLE VENTA
( 
	IDVENTA              integer  NOT NULL ,
	IDCLIENTE            integer  NOT NULL ,
	FECHA                datetime  NOT NULL ,
	IMPORTE              numeric(12,2)  NOT NULL ,
	IMPUESTO             numeric(12,2)  NOT NULL ,
	TOTAL                numeric(12,2)  NOT NULL ,
	CONSTRAINT XPKVENTA PRIMARY KEY (IDVENTA ASC),
	CONSTRAINT FK_VENTA_CLIENTE FOREIGN KEY (IDCLIENTE) REFERENCES dbo.CLIENTE(IDCLIENTE)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE dbo.DETALLEVENTA
( 
	IDPRODUCTO           integer  NOT NULL ,
	IDVENTA              integer  NOT NULL ,
	PRECIO               numeric(12,2)  NOT NULL ,
	CANTIDAD             integer  NOT NULL ,
	SUBTOTAL             numeric(12,2)  NOT NULL ,
	CONSTRAINT XPKDETALLEVENTA PRIMARY KEY (IDPRODUCTO ASC,IDVENTA ASC),
	CONSTRAINT FK_DETALLEVENTA_PRODUCTO FOREIGN KEY (IDPRODUCTO) REFERENCES dbo.PRODUCTO(IDPRODUCTO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
  CONSTRAINT FK_DETALLEVENTA_VENTA FOREIGN KEY (IDVENTA) REFERENCES VENTA(IDVENTA)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go


